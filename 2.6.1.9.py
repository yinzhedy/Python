# input a float value for variable a here
a = float(input("Please enter a number:"))
# input a float value for variable b here
b = float(input("Please enter another number:"))

# output the result of addition here
add = a+b
print("The result of adding " + str(a) + "to" + str(b) + " is equal to:" , add)
# output the result of subtraction here
subtract = a-b
print("The result of subtracting " + str(b) + "from" + str(a) + " is equal to:" , subtract)
# output the result of multiplication here
multiply = a*b
print("The result of multiplying " + str(a) + "by" + str(b) + " is equal to:" , multiply)
# output the result of division here
divide = a/b
print("The result of dividing " + str(a) + "by" + str(b) + " is equal to:" , divide)

